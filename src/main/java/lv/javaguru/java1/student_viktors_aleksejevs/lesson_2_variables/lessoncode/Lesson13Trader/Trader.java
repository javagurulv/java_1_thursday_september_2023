package lv.javaguru.java1.student_viktors_aleksejevs.lesson_2_variables.lessoncode.Lesson13Trader;

public class Trader {

    private String name;
    private String city;

    public Trader(String name, String city) {
        this.name = name;
        this.city = city;
    }

    public static String getName(String t) {
        return name;
    }

    public static String getCity(String t) {
        return city;
    }
}
