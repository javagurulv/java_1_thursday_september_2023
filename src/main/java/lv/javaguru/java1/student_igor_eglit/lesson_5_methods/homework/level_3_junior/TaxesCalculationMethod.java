class TaxesCalculationMethod {
    double result = 0;

    double lowTax(int money) {
        return result = money * 0.3;
    }

    double midTax(int money) {
        return result = money * 0.4;
    }

    double highTax(int money) {
        return result = money * 0.5;
    }
}
