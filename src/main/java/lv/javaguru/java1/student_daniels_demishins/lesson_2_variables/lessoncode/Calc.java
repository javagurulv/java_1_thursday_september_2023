package lv.javaguru.java1.student_daniels_demishins.lesson_2_variables.lessoncode;

public class Calc {
    public static void main(String[] args) {
        int numA = 10;
        int numB = 5;

        int sum = numA + numB;
        int min = numA - numB;
        int mul = numA * numB;
        int div = numA / numB;

        System.out.println("Result + = " + sum);
        System.out.println("Result - = " + min);
        System.out.println("Result * = " + mul);
        System.out.println("Result / = " + div);
    }
}
