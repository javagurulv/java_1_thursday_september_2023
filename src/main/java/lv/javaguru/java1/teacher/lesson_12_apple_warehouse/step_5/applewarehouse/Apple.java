package lv.javaguru.java1.teacher.lesson_12_apple_warehouse.step_5.applewarehouse;

class Apple {

    private String color;
    private int weight;

    public Apple (String color, int weight) {
        this.color = color;
        this.weight = weight;
    }

    public String getColor() {
        return color;
    }

    public int getWeight() {
        return weight;
    }

}
