package lv.javaguru.java1.student_valera_kovs.lesson7.lvl3;

class CashReceipt {

    String number;
    double sum;

    public CashReceipt(String number, double sum) {
        this.number = number;
        this.sum = sum;
    }

    public String getNumber() {
        return number;
    }

    public double getSum() {
        return sum;
    }

}

