package lv.javaguru.java1.student_valera_kovs.lesson12;

class Apple {

   private String color;

   private int weight;

    public Apple(String color, int weight) {
        this.color = color;
        this.weight = weight;
    }

    public String getColor() {
        return color;
    }

    public int getWeight() {
        return weight;
    }

}
