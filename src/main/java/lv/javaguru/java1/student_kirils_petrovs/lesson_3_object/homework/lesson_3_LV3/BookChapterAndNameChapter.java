package lv.javaguru.java1.student_kirils_petrovs.lesson_3_object.homework.lesson_3_LV3;

class BookChapterAndNameChapter {




    private String Chapter1;
    private String  NameChapter1;



    private String Chapter2;
    private String  NameChapter2;


    public BookChapterAndNameChapter(String Chapter1, String NameChapter1, String Chapter2, String  NameChapter2){

       this.Chapter1 = Chapter1;
       this.NameChapter1 = NameChapter1;

       this.Chapter2 = Chapter2;
       this.NameChapter2 = NameChapter2;
    }

    public String getChapter1() {
        return Chapter1;
    }

    public String getNameChapter1() {
        return NameChapter1;
    }

    public String getChapter2() {
        return Chapter2;
    }

    public String getNameChapter2() {
        return NameChapter2;
    }
}

