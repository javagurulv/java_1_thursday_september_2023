package lv.javaguru.java1.student_kirils_petrovs.lesson_1_introduction.lessoncode.country;

public class Country {


    private String countryName;
        private String coudeCountr;
        private int population;


        public Country(String countryName, String coudeCountry, int population) {
            this.countryName = countryName;
            this.coudeCountr = coudeCountry;
            this.population = population;
        }

        public String getCountryName() {
            return countryName;
        }

        public String getCoudeCountr() {
            return coudeCountr;
        }

        public int getPopulation() {
            return population;
        }
    }






