package lv.javaguru.java1.student_jelena_vavere.lesson_2_variables.homework;

public class CalculatorDoubleNumbers {
    public static void main(String[] args) {
        double number1 = 1.5;
        double number2 = 2.7;

        double additionResult = number1 + number2;
        double subtractionResult = number1 - number2;
        double multiplicationResult = number1 * number2;
        double divisionResult = number1 / number2;

        System.out.println("Result + = " + additionResult);
        System.out.println("Result _ = " + subtractionResult);
        System.out.println("Result * = " + multiplicationResult);
        System.out.println("Result /  = " + divisionResult);

    }
}



